/**
 * One-off: split the legacy bundled library packs (ADR-010) into per-type
 * content modules (ADR-014). Each themed pack becomes up to three modules — a
 * flat category module, a list-folder module, and a sentence-folder module —
 * carrying the pack's tier. The starter pack is skipped (its content is the
 * hand-curated default folders, published separately).
 *
 * Pack item shapes are identical to module item shapes, so this is a faithful
 * re-pack (images/audio/symbols preserved). After writing the module JSONs it
 * regenerates each tree's `_index.ts` barrel by scanning the directory.
 *
 * Run:  node scripts/convert-packs-to-modules.mjs
 * Then: a migration publishes the new modules (lifecycle rows) — see
 *       convex/migrations.ts:publishConvertedPackModules.
 */
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join } from "node:path";

const PACKS_DIR = "convex/data/library_packs";
const OUT = {
  categories: "convex/data/categories",
  lists: "convex/data/lists",
  sentences: "convex/data/sentences",
};
const MODULE_TYPE = {
  categories: "CategoryModule",
  lists: "ListModule",
  sentences: "SentenceModule",
};
const MAP_NAME = {
  categories: "CATEGORY_MODULES",
  lists: "LIST_MODULES",
  sentences: "SENTENCE_MODULES",
};

const PROVENANCE = { author: "Mo Speech", version: "1", licence: "proprietary" };

// A few legacy packs used this non-resolving placeholder cover (it lives under
// `static/`, not R2, so `/api/assets` can't serve it). When a pack carries it
// (or no cover) we fall back to the module's own first content image, so the
// library card matches what the user sees once it's installed.
const PLACEHOLDER_COVER = "static/pack-cover-default.webp";

/** A path that `/api/assets` can actually serve (not the `static/` placeholder). */
function isResolvable(p) {
  return !!p && p !== PLACEHOLDER_COVER && !p.startsWith("static/");
}

/** First real image inside a module's content — the natural card cover. */
function representativeImage(tree, items) {
  const first = items[0];
  if (!first) return undefined;
  if (tree === "categories") return first.imagePath; // category's own folder image
  if (tree === "lists") return first.items?.[0]?.imagePath;
  if (tree === "sentences") return first.slots?.[0]?.imagePath;
  return undefined;
}

/** Build one module object in a stable key order (clean git diffs). */
function buildModule(pack, tree, items) {
  let cover = isResolvable(pack.coverImagePath) ? pack.coverImagePath : undefined;
  if (!cover) {
    const rep = representativeImage(tree, items);
    if (isResolvable(rep)) cover = rep;
  }
  return {
    slug: pack.slug,
    tree,
    name: pack.name,
    description: pack.description,
    ...(cover ? { coverImagePath: cover } : {}),
    defaultTier: pack.defaultTier,
    provenance: PROVENANCE,
    items,
  };
}

const packFiles = readdirSync(PACKS_DIR).filter(
  (f) => f.endsWith(".json") && f !== "_starter.json"
);

let written = 0;
for (const file of packFiles) {
  const pack = JSON.parse(readFileSync(join(PACKS_DIR, file), "utf8"));
  const byTree = {
    categories: pack.categories ?? [],
    lists: pack.lists ?? [],
    sentences: pack.sentences ?? [],
  };
  for (const tree of ["categories", "lists", "sentences"]) {
    const items = byTree[tree];
    if (!items.length) continue;
    const mod = buildModule(pack, tree, items);
    const path = join(OUT[tree], `${pack.slug}.json`);
    writeFileSync(path, JSON.stringify(mod, null, 2) + "\n");
    written++;
    console.log(`  ${tree}/${pack.slug}.json  (tier=${pack.defaultTier}, items=${items.length})`);
  }
}

/** camelCase a slug for a JS import binding (christmas → christmas, test-x → testX). */
function ident(slug) {
  return slug.replace(/[^a-zA-Z0-9]+(.)?/g, (_, c) => (c ? c.toUpperCase() : ""));
}

/** Regenerate a tree barrel from the *.json files present in its directory. */
function regenBarrel(tree) {
  const dir = OUT[tree];
  const type = MODULE_TYPE[tree];
  const mapName = MAP_NAME[tree];
  const slugs = readdirSync(dir)
    .filter((f) => f.endsWith(".json"))
    .map((f) => f.replace(/\.json$/, ""))
    .sort();
  const imports = slugs
    .map((s) => `import ${ident(s)} from "./${s}.json";`)
    .join("\n");
  const entries = slugs
    .map((s) => `  ${JSON.stringify(s)}: ${ident(s)} as ${type},`)
    .join("\n");
  const body = `/**
 * ${type} catalogue barrel (ADR-014 §1). AUTO-GENERATED by
 * scripts/convert-packs-to-modules.mjs and the curation publish route — add
 * modules by dropping <slug>.json here and re-running, not by hand-editing.
 */

import type { ${type} } from "../_shared/types";

// ── Module imports ────────────────────────────────────────────────────────────
${imports || "// (none yet)"}

// ── Catalogue map ─────────────────────────────────────────────────────────────
export const ${mapName}: Record<string, ${type}> = {
${entries}
};
`;
  writeFileSync(join(dir, "_index.ts"), body);
  console.log(`  regenerated ${tree}/_index.ts (${slugs.length} modules)`);
}

console.log(`Wrote ${written} module files.`);
for (const tree of ["categories", "lists", "sentences"]) regenBarrel(tree);
console.log("Done.");
